import sys

def huffman_encoding(data):
    if data is None:
        return ("No data to encode"), None

    count = {}

    for i in data:
        if i in count.keys():
            count[i] += 1
        else:
            count.update({i: 1})

    vals = {}
    bc = '1'
    for c in sorted(count.items()):
        vals[c[0]] = bc
        bc = '0' + bc

    encode = ''
    for d in data:
        encode += vals[d]

    return encode, vals


def huffman_decoding(data,tree):
    if data is None:
        return ("No data to decode")

    vals = {}

    for t in tree:
        vals[tree[t]] = t
    bc = ''
    decode = ''
    for d in data:
        if d == '1':
            decode += vals[bc + d]
            bc = ''
        else:
            bc += d

    return decode

